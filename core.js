// ── Detect คำสำคัญ ────────────────────────────────────────────
const DISPENSE_TRIGGERS = [
  'จ่ายยา', 'แนะนำยา', 'ให้ยา', 'จะให้', 'ขอจ่าย', 'จะจ่าย',
  'เสนอยา', 'มียาที่', 'แนะนำให้ใช้', 'ควรใช้ยา'
];

const CLOSING_TRIGGERS = [
  'ขอบคุณ', 'ขอบใจ', 'ขอบคุณมาก', 'ขอบคุณนะ', 'ขอบคุณครับ', 'ขอบคุณค่ะ',
  'ขอบคุณมากครับ', 'ขอบคุณมากค่ะ', 'ลาก่อน', 'แล้วเจอกัน', 'ไปก่อนนะ'
];

function detectDispense(text) {
  return DISPENSE_TRIGGERS.some(t => text.includes(t));
}

function detectClosing(text) {
  return CLOSING_TRIGGERS.some(t => text.includes(t));
}
